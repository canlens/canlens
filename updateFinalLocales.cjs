const updateLocale = (file, dataToAdd) => {
  const path = './src/i18n/locales/' + file;
  let data = JSON.parse(fs.readFileSync(path, 'utf8'));
  data = { ...data, ...dataToAdd };
  fs.writeFileSync(path, JSON.stringify(data, null, 2));
};

